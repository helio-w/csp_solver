package solver;

import java.util.Stack;
import java.util.ArrayList;
import var.Variable;

public class StackBackTrack extends Stack<ArrayList<Variable>>{

    public StackBackTrack() {
        super();
    }

    @Override
    public ArrayList<Variable> pop() {
        return super.pop();
    }

    @Override
    public ArrayList<Variable> push(ArrayList<Variable> p) {
        return super.push(p);
    }
}

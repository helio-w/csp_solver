package solver;

import java.util.Stack;
import java.util.ArrayList;

import var.IntCsp;

public class StackBackTrack extends Stack<ArrayList<IntCsp>>{

    public StackBackTrack() {
        super();
    }

    @Override
    public ArrayList<IntCsp> pop() {
        return super.pop();
    }

    @Override
    public ArrayList<IntCsp> push(ArrayList<IntCsp> p) {
        return super.push(p);
    }
}
